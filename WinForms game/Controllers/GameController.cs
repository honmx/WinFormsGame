        public static List<PointF> GetByNumber(int number)
        {
            var positions = new List<PointF>();

            for (var i = 0; i < map.GetLength(0); i++)
                for (var j = 0; j < map.GetLength(1); j++)
                {
                    if (map[i, j] == 0) continue;
                    
                    if (map[i, j] == number)
                    {
                        positions.Add(new Point(j * 40 - 30, i * 50));
                    }
                }

            return positions;
        }

